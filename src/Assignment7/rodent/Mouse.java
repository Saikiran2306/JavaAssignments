package Assignment7.rodent;


public class Mouse extends Rodent {
    public Mouse() {
        System.out.println("I am Mouse");
    }

    public void eat() {
        System.out.println("Mouse is eating ...");
    }

    public void sleep() {
        System.out.println("Mouse is sleeping ...");
    }

    public void run() {
        System.out.println("Mouse is running ...");
    }
}