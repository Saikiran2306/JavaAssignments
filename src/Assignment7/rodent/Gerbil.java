package Assignment7.rodent;

public class Gerbil extends Rodent {
    public Gerbil() {
        System.out.println("I am Gerbil");
    }

    public void eat() {
        System.out.println("Gerbil is eating ...");
    }

    public void sleep() {
        System.out.println("Gerbil is sleeping ...");
    }

    public void run() {
        System.out.println("Gerbil is running ...");
    }
}